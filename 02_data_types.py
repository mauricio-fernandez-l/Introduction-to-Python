# %% String

a = "String"
print(type(a), a)

# %% Integer

a = 2
print(type(a), a)

# %% Decimal/float

a = 2.0
print(type(a), a)

# %% Boolean

a = True
print(type(a), a)

# %% List

a = ["string", 2, 2.0]
print(type(a), a)
print(a[0])
print(a[1])
print(a[2])

# %% Dictionary

a = {
    "Mauricio": "Chile",
    "Hanna": "Poland",
    "Antonia": "Germany",
    "Greta": "Germany",
    "Isabella": "Germany",
}
print(type(a), a)
print(a["Hanna"])
